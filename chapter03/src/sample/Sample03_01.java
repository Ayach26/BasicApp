package sample;

public class Sample03_01 {

  public static void main(String[] args) {
    System.out.println(127L);           //  long型
    System.out.println(0x7f);           //  16進数
    System.out.println(0177);           //  8進数
    System.out.println(0b0111_1111);    //  2進数

    System.out.println(12.34);          //  double型
    System.out.println(1234e-2);        //  指数形式
  }
}